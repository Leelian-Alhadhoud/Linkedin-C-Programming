#include <stdio.h>

int main() 
{
    printf("You are a %s\n" , "Programmer");

    /* %s is a placeholder which represents strings 
    in this example the %s presents the string programmer  
    which is the secound argument in the printf function */

    printf("You are a %c %s\n" , 'C' ,"Programmer");
    /* %c is a single character placeholder */ 
     
    return(0);
}